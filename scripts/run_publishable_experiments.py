def plot_retail(rows: List[Dict[str, object]], seg_rows: List[Dict[str, object]], conc_rows: List[Dict[str, object]]) -> None:
    import matplotlib.pyplot as plt

    apply_plot_style()
    base_rows = [r for r in rows if int(r["attack_budget"]) == int(r["gamma"])]
    gammas = sorted({int(r["gamma"]) for r in base_rows})
    x = np.array([g / 300 for g in gammas])
    fig, axes = plt.subplots(1, 3, figsize=(7.2, 2.4))
    for ax, value, ylabel, title in [
        (axes[0], "revenue_ratio", "Revenue ratio", "(a) Revenue"),
        (axes[1], "z_gamma", r"$Z_\Gamma(\mathbf{x})$", "(b) Certificate"),
        (axes[2], "violation_iid", "IID violation probability", "(c) IID stress"),
    ]:
        meds, los, his = [], [], []
        for gamma in gammas:
            vals = [float(r[value]) for r in base_rows if int(r["gamma"]) == gamma]
            meds.append(median(vals))
            los.append(q(vals, 0.25))
            his.append(q(vals, 0.75))
        ax.plot(x, meds, marker="o", color="#4c78a8")
        ax.fill_between(x, los, his, color="#4c78a8", alpha=0.18)
        if value == "z_gamma":
            ax.axhline(0.0, color="black", linestyle="--", linewidth=0.8)
        ax.axvline(17 / 300, color="0.45", linestyle=":", linewidth=0.8)
        ax.set_xlabel(r"$\Gamma/n$")
        ax.set_ylabel(ylabel)
        ax.set_title(title)
    axes[2].set_ylim(bottom=0.0)
    fig.tight_layout()
    fig.savefig(FIG_DIR / "retail_frontier.pdf")
    plt.close(fig)

    gamma_star = 17
    segs = [seg["name"] for seg in SEGMENTS]
    colors = ["#4c78a8", "#f58518", "#54a24b", "#e45756"]
    signed = [[float(r["avg_signed_price_change"]) for r in seg_rows if int(r["gamma"]) == gamma_star and r["segment"] == s] for s in segs]
    changed = [[float(r["share_changed"]) for r in seg_rows if int(r["gamma"]) == gamma_star and r["segment"] == s] for s in segs]
    fig, axes = plt.subplots(1, 2, figsize=(6.7, 2.45))
    xidx = np.arange(len(segs))
    signed_med = [median(v) for v in signed]
    signed_err = np.array([[median(v) - q(v, 0.25) for v in signed], [q(v, 0.75) - median(v) for v in signed]])
    axes[0].bar(xidx, signed_med, yerr=signed_err, color=colors, capsize=2, linewidth=0.6, edgecolor="0.25")
    axes[0].axhline(0.0, color="black", linewidth=0.8)
    axes[0].set_ylabel("Signed price change")
    axes[0].set_title("(a) Direction")
    changed_med = [median(v) for v in changed]
    changed_err = np.array([[median(v) - q(v, 0.25) for v in changed], [q(v, 0.75) - median(v) for v in changed]])
    axes[1].bar(xidx, changed_med, yerr=changed_err, color=colors, capsize=2, linewidth=0.6, edgecolor="0.25")
    axes[1].set_ylim(0.0, 1.0)
    axes[1].set_ylabel("Share changed")
    axes[1].set_title("(b) Incidence")
    for ax in axes:
        ax.set_xticks(xidx, segs, rotation=20, ha="right")
    fig.suptitle(r"Segment diagnostics at $\Gamma=\lfloor\sqrt{n}\rfloor$", y=1.02, fontsize=8.6)
    fig.tight_layout()
    fig.savefig(FIG_DIR / "retail_segment_adjustments.pdf")
    plt.close(fig)

    fig, ax = plt.subplots(figsize=(4.6, 2.35))
    for metric, label, color in [
        ("top10_margin_share", "top-10 share", "#4c78a8"),
        ("hhi_positive_margin", "HHI", "#f58518"),
        ("gini_positive_margin", "Gini", "#54a24b"),
    ]:
        y, lo, hi = [], [], []
        for g in gammas:
            vals = [float(r[metric]) for r in conc_rows if int(r["gamma"]) == g]
            y.append(median(vals))
            lo.append(q(vals, 0.25))
            hi.append(q(vals, 0.75))
        ax.plot(x, y, marker="o", label=label, color=color)
        ax.fill_between(x, lo, hi, color=color, alpha=0.10, linewidth=0)
    ax.axvline(17 / 300, color="0.45", linestyle=":", linewidth=0.8)
    ax.set_xlabel(r"$\Gamma/n$")
    ax.set_ylabel("Concentration")
    ax.set_title("Positive margin concentration")
    ax.legend(loc="best")
    fig.tight_layout()
    fig.savefig(FIG_DIR / "retail_margin_concentration.pdf")
    plt.close(fig)


def make_tables_and_numbers(
    availability: Dict[str, object],
    gap_rows: List[Dict[str, object]],
    exact_rows: List[Dict[str, object]],
    scale_rows: List[Dict[str, object]],
    cert_rows: List[Dict[str, object]],
    retail_rows: List[Dict[str, object]],
    retail_seg: List[Dict[str, object]],
    retail_conc: List[Dict[str, object]],
) -> None:
    ratios = [float(r["l_rd_over_delta"]) for r in gap_rows]
    n_gaps = [float(r["n_gap_lp"]) for r in gap_rows]
    violations = [r for r in gap_rows if float(r["l_rd"]) > float(r["delta_v_max_theta"]) + 1e-7]
    certified = [r for r in exact_rows if math.isfinite(float(r["hullround_rel_gap_vs_certified"]))]
    certified_gaps = [float(r["hullround_rel_gap_vs_certified"]) for r in certified]
    scale_500 = [float(r["hullround_runtime_s"]) for r in scale_rows if int(r["n"]) == 500]
    retail_base = [r for r in retail_rows if int(r["attack_budget"]) == int(r["gamma"])]
    retail_sqrt = [r for r in retail_base if int(r["gamma"]) == 17]
    retail_gamma0 = [r for r in retail_conc if int(r["gamma"]) == 0]
    retail_gamma17 = [r for r in retail_conc if int(r["gamma"]) == 17]

    macros = {
        "PublishableGapSeeds": len({r["seed"] for r in gap_rows}),
        "PublishableGapNMax": max(int(r["n"]) for r in gap_rows),
        "GapRatioMedian": fmt(median(ratios), 3),
        "GapRatioPtfive": fmt(q(ratios, 0.95), 3),
        "GapRatioMax": fmt(max(ratios), 3),
        "GapBoundViolationCount": len(violations),
        "ScaledGapMedian": fmt(median(n_gaps), 3),
        "ExactCertifiedCount": len(certified),
        "ExactTotalCount": len(exact_rows),
        "ExactBenchmarkMedianGap": fmt_pct(median(certified_gaps), 2),
        "ExactBenchmarkMaxGap": fmt_pct(max(certified_gaps) if certified_gaps else float("nan"), 2),
        "ScipStatusText": "available" if availability.get("scip_available") else "not available",
        "HighsStatusText": "available" if availability.get("highs_available") else "not available",
        "OpenSolverName": "SCIP/HiGHS" if availability.get("scip_available") else "HiGHS",
        "ScaleRuntimeNfivehundredMedian": fmt(median(scale_500), 2),
        "SyntheticFrontierSeedCount": len({r["seed"] for r in cert_rows}),
        "RetailSeedCount": len({r["seed"] for r in retail_rows}),
        "RetailSqrtGammaRevenueLossMedian": fmt_pct(1.0 - median([float(r["revenue_ratio"]) for r in retail_sqrt]), 2),
        "RetailSqrtGammaViolationMedian": fmt_pct(median([float(r["violation_iid"]) for r in retail_sqrt]), 2),
        "RetailSqrtGammaResidualMedian": fmt(median([float(r["z_gamma"]) for r in retail_sqrt]), 2),
        "RetailTopTenShareGammaZero": fmt_pct(median([float(r["top10_margin_share"]) for r in retail_gamma0]), 2),
        "RetailTopTenShareGammaSqrt": fmt_pct(median([float(r["top10_margin_share"]) for r in retail_gamma17]), 2),
    }
    lines = ["% Auto-generated by scripts/run_publishable_experiments.py"]
    for name, value in macros.items():
        lines.append(f"\\newcommand{{\\{name}}}{{{value}}}")
    (OUT / "auto_numbers.tex").write_text("\n".join(lines) + "\n", encoding="utf-8")

    exact_groups: Dict[Tuple[int, str], List[Dict[str, object]]] = {}
    for r in exact_rows:
        exact_groups.setdefault((int(r["n"]), str(r["gamma_regime"])), []).append(r)
    exact_lines = [
        r"\begin{tabular}{llrrrrrr}",
        r"\toprule",
        r"$n$ & $\Gamma$ & cert. & med. gap & max gap & HullRound s & Full s & Enum s \\",
        r"\midrule",
    ]
    for (n, regime), group in sorted(exact_groups.items(), key=lambda kv: (kv[0][0], kv[0][1])):
        cert_group = [r for r in group if math.isfinite(float(r["hullround_rel_gap_vs_certified"]))]
        gaps = [float(r["hullround_rel_gap_vs_certified"]) for r in cert_group]
        exact_lines.append(
            f"{n} & {regime} & {len(cert_group)}/{len(group)} & {fmt_pct(median(gaps), 2)} & "
            f"{fmt_pct(max(gaps) if gaps else float('nan'), 2)} & "
            f"{fmt(median([float(r['hullround_runtime_s']) for r in group]), 2)} & "
            f"{fmt(median([float(r['full_runtime_s']) for r in group]), 2)} & "
            f"{fmt(median([float(r['theta_enum_runtime_s']) for r in group]), 2)} \\\\"
        )
    exact_lines += [r"\bottomrule", r"\end{tabular}"]
    (TABLE_DIR / "exact_benchmark_table.tex").write_text("\n".join(exact_lines) + "\n", encoding="utf-8")

    scale_groups: Dict[Tuple[int, int], List[Dict[str, object]]] = {}
    for r in scale_rows:
        scale_groups.setdefault((int(r["n"]), int(r["m"])), []).append(r)
    scale_lines = [
        r"\begin{tabular}{rrrrrr}",
        r"\toprule",
        r"$n$ & $m$ & HullRound s & median $Z$ & HiGHS cert. & hull/raw \\",
        r"\midrule",
    ]
    for (n, m), group in sorted(scale_groups.items()):
        attempted = [r for r in group if str(r.get("full_status")) != "NOT_RUN"]
        opt_count = sum(1 for r in attempted if bool(r["full_certified"]))
        generic_cell = f"{opt_count}/{len(attempted)}" if attempted else r"\multicolumn{1}{c}{n/r}"
        scale_lines.append(
            f"{n} & {m} & {fmt(median([float(r['hullround_runtime_s']) for r in group]), 2)} & "
            f"{fmt(median([float(r['hullround_certificate']) for r in group]), 1)} & "
            f"{generic_cell} & {fmt(median([float(r['hull_compression_ratio']) for r in group]), 2)} \\\\"
        )
    scale_lines += [r"\bottomrule", r"\end{tabular}"]
    (TABLE_DIR / "scalability_table.tex").write_text("\n".join(scale_lines) + "\n", encoding="utf-8")


def save_environment(availability: Dict[str, object], design: Design) -> None:
    env = {
        "python_version": sys.version,
        "platform": platform.platform(),
        "processor": platform.processor(),
        "machine": platform.machine(),
        "package_versions": {},
        "design": design.__dict__,
        "solver_availability": availability,
    }
    for mod in ["numpy", "matplotlib", "scipy"]:
        try:
            module = __import__(mod)
            env["package_versions"][mod] = getattr(module, "__version__", "unknown")
        except Exception as exc:
            env["package_versions"][mod] = f"unavailable: {exc}"
    (LOG_DIR / "environment.json").write_text(json.dumps(env, indent=2, default=str), encoding="utf-8")
    (LOG_DIR / "solver_availability.json").write_text(json.dumps(availability, indent=2, default=str), encoding="utf-8")


def validate_outputs() -> Dict[str, object]:
    checks: Dict[str, object] = {}
    gap = read_csv(CSV_DIR / "synthetic_gap_replicates.csv")
    ratios = [float(r["l_rd_over_delta"]) for r in gap]
    checks["gap_rows"] = len(gap)
    checks["gap_bound_violations"] = sum(
        1 for r in gap if float(r["l_rd"]) > float(r["delta_v_max_theta"]) + 1e-7
    )
    checks["max_l_rd_over_delta"] = max(ratios) if ratios else float("nan")
    checks["lp_highs_validation_count"] = sum(
        1 for r in gap if math.isfinite(float(r.get("lp_highs_abs_diff", "nan")))
    )
    checks["lp_highs_max_abs_diff"] = max(
        [float(r["lp_highs_abs_diff"]) for r in gap if math.isfinite(float(r.get("lp_highs_abs_diff", "nan")))] or [0.0]
    )
    for name in [
        "gap_replicates.pdf",
        "solver_benchmark_runtime.pdf",
        "solver_benchmark_gap.pdf",
        "risk_frontier_certified.pdf",
        "scalability.pdf",
        "retail_frontier.pdf",
        "retail_segment_adjustments.pdf",
        "retail_margin_concentration.pdf",
    ]:
        checks[f"figure_{name}"] = (FIG_DIR / name).exists()
    (LOG_DIR / "validation.json").write_text(json.dumps(checks, indent=2), encoding="utf-8")
    if checks["gap_bound_violations"] != 0:
        raise AssertionError("round-down bound violation detected")
    if checks["lp_highs_max_abs_diff"] > 1e-6:
        raise AssertionError("LP validation failed")
    return checks


def main() -> None:
    parser = argparse.ArgumentParser()
    parser.add_argument("--smoke", action="store_true", help="run a tiny end-to-end smoke test")
    args = parser.parse_args()

    ensure_dirs()
    design = choose_design(args.smoke)
    availability = detect_gurobi()
    save_environment(availability, design)
    print("[solver]", json.dumps(availability), flush=True)

    gap_rows = run_gap_experiment(design)
    exact_rows, scale_rows = run_solver_benchmark(design, availability)
    cert_rows, mc_rows = run_frontier(design)
    retail_rows, retail_seg, retail_conc = run_retail(design)

    plot_gap(gap_rows)
    plot_solver(exact_rows, scale_rows)
    plot_frontier(cert_rows, mc_rows)
