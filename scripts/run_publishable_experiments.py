def robust_residual_for_budget(instance: PricingInstance, selections: Sequence[int], budget: int) -> float:
    s_vals = np.array([instance.items[i][j].margin for i, j in enumerate(selections)], dtype=float)
    t_vals = np.abs(np.array([instance.items[i][j].uncertainty for i, j in enumerate(selections)], dtype=float))
    k = int(max(0, min(int(budget), len(selections))))
    penalty = float(np.sum(np.sort(t_vals)[-k:])) if k > 0 else 0.0
    return float(np.sum(s_vals) - penalty)


def raw_theta_candidates(instance: PricingInstance) -> np.ndarray:
    vals = [0.0]
    for group in instance.items:
        vals.extend(abs(opt.uncertainty) for opt in group)
    return np.unique(np.array(vals, dtype=float))


def option_arrays(instance: PricingInstance) -> Tuple[List[np.ndarray], List[np.ndarray], List[np.ndarray]]:
    return extract_arrays(instance)


def fixed_theta_costs(
    instance: PricingInstance, theta: float
) -> Tuple[List[np.ndarray], List[np.ndarray], float, float]:
    values, margins, uncertainties = option_arrays(instance)
    costs: List[np.ndarray] = []
    s_star_sum = 0.0
    for s, t in zip(margins, uncertainties):
        s_theta = s - np.maximum(0.0, np.abs(t) - theta)
        s_star = float(np.max(s_theta))
        s_star_sum += s_star
        costs.append(np.maximum(s_star - s_theta, 0.0))
    capacity = s_star_sum - float(instance.gamma) * float(theta)
    return values, costs, s_star_sum, capacity


def hullround_metrics(instance: PricingInstance, *, validate_lp: bool = False) -> Dict[str, object]:
    sol = solve(instance, upgrade_completion=True)
    if not sol.is_feasible:
        raise RuntimeError("HullRound did not return a robust-feasible solution")

    values, margins, uncertainties = option_arrays(instance)
    hulls, s_star_sum = compute_hulls_for_theta(values, margins, uncertainties, sol.theta)
    capacity = float(s_star_sum - instance.gamma * sol.theta)
    lp = greedy_lp(hulls, capacity)
    rd = round_down_value(lp, hulls, values)
    bound = max(0.0, delta_v_max(hulls))
    l_rd = float(lp.lp_value - rd)
    frac_count = sum(1 for pos in lp.positions if EPS < pos.lambda_ < 1.0 - EPS)
    cert = compute_certificate(instance, sol.selections)

    lp_highs_obj = float("nan")
    lp_highs_abs_diff = float("nan")
    if validate_lp:
        values_full, costs_full, _, _ = fixed_theta_costs(instance, sol.theta)
        highs = solve_lp_highs_full(values_full, costs_full, capacity)
        if highs is not None:
            lp_highs_obj = float(highs["objective"])
            lp_highs_abs_diff = abs(lp_highs_obj - float(lp.lp_value))

    if cert < -1e-7:
        raise AssertionError(f"certificate violation: {cert}")
    if frac_count > 1:
        raise AssertionError(f"LP has more than one fractional item: {frac_count}")
    if l_rd > bound + 1e-7:
        raise AssertionError(f"round-down loss {l_rd} exceeds Delta V max {bound}")
    if validate_lp and math.isfinite(lp_highs_abs_diff) and lp_highs_abs_diff > 1e-6:
        raise AssertionError(f"hull LP disagrees with HiGHS by {lp_highs_abs_diff}")

    instr = (sol.metadata or {}).get("instrumentation", {})
    return {
        "theta": float(sol.theta),
        "lp_objective": float(lp.lp_value),
        "round_down_objective": float(rd),
        "final_objective": float(sol.objective),
        "l_rd": l_rd,
        "delta_v_max_theta": bound,
        "l_rd_over_delta": l_rd / bound if bound > EPS else 0.0,
        "delta_v_max_over_lp_ub": bound / max(abs(float(lp.lp_value)), 1.0),
        "gap_lp": l_rd / float(lp.lp_value) if abs(lp.lp_value) > EPS else float("nan"),
        "n_gap_lp": instance.n_items * l_rd / float(lp.lp_value) if abs(lp.lp_value) > EPS else float("nan"),
        "certificate_value": float(cert),
        "runtime_total_s": float(sol.elapsed),
        "fractional_items": int(frac_count),
        "candidate_count_raw": int(instr.get("candidate_count_raw", 0)),
        "candidate_count_reduced": int(instr.get("candidate_count_reduced", 0)),
        "theta_evaluated_count": int(instr.get("theta_evaluated_count", 0)),
        "theta_skipped_capacity_count": int(instr.get("theta_skipped_capacity_count", 0)),
        "total_hull_rebuilds": int(instr.get("total_hull_rebuilds", 0)),
        "lp_highs_objective": lp_highs_obj,
        "lp_highs_abs_diff": lp_highs_abs_diff,
        "selections": sol.selections,
    }


