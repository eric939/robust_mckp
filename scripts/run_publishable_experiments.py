def write_csv(path: Path, rows: List[Dict[str, object]]) -> None:
    if not rows:
        path.write_text("", encoding="utf-8")
        return
    keys: List[str] = []
    for row in rows:
        for key in row:
            if key not in keys:
                keys.append(key)
    with path.open("w", newline="", encoding="utf-8") as fh:
        writer = csv.DictWriter(fh, fieldnames=keys)
        writer.writeheader()
        writer.writerows(rows)


def read_csv(path: Path) -> List[Dict[str, str]]:
    with path.open(newline="", encoding="utf-8") as fh:
        return list(csv.DictReader(fh))


def finite(values: Iterable[float]) -> List[float]:
    return [float(x) for x in values if math.isfinite(float(x))]


def q(values: Sequence[float], pct: float) -> float:
    vals = finite(values)
    if not vals:
        return float("nan")
    return float(np.quantile(np.array(vals, dtype=float), pct))


def median(values: Sequence[float]) -> float:
    vals = finite(values)
    return float(statistics.median(vals)) if vals else float("nan")


def fmt(x: float, digits: int = 3) -> str:
    if not math.isfinite(float(x)):
        return "--"
    return f"{float(x):.{digits}f}"


def fmt_pct(x: float, digits: int = 2) -> str:
    if not math.isfinite(float(x)):
        return "--"
    return f"{100.0 * float(x):.{digits}f}\\%"


