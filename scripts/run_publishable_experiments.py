def scipy_opt():
    try:
        import scipy.optimize as opt

        return opt
    except Exception:
        return None


def highs_status_name(status: int) -> str:
    return {0: "OPTIMAL", 1: "TIME_LIMIT", 2: "INFEASIBLE", 3: "UNBOUNDED", 4: "NUMERIC"}.get(
        int(status), f"STATUS_{status}"
    )


def solve_full_robust_highs(
    instance: PricingInstance, *, time_limit: float = 60.0
) -> Dict[str, object]:
    opt = scipy_opt()
    if opt is None or not hasattr(opt, "milp"):
        return {"status": "UNAVAILABLE", "certified": False}

    values, margins, uncertainties = option_arrays(instance)
    sizes = [len(v) for v in values]
    n = len(sizes)
    z_total = int(sum(sizes))
    u_offset = z_total
    pi_offset = z_total + n
    theta_idx = z_total + 2 * n
    nvars = z_total + 2 * n + 1

    c = np.zeros(nvars, dtype=float)
    c[:z_total] = -np.concatenate(values)
    lb = np.zeros(nvars, dtype=float)
    ub = np.full(nvars, np.inf, dtype=float)
    ub[:z_total] = 1.0
    integrality = np.zeros(nvars, dtype=int)
    integrality[:z_total] = 1

    rows = []
    lows = []
    ups = []

    offset = 0
    for i, m_i in enumerate(sizes):
        row = np.zeros(nvars, dtype=float)
        row[offset : offset + m_i] = 1.0
        rows.append(row)
        lows.append(1.0)
        ups.append(1.0)
        offset += m_i

    offset = 0
    for i, (m_i, t) in enumerate(zip(sizes, uncertainties)):
        row = np.zeros(nvars, dtype=float)
        row[u_offset + i] = 1.0
        row[offset : offset + m_i] = -np.abs(t)
        rows.append(row)
        lows.append(0.0)
        ups.append(0.0)
        offset += m_i

    for i in range(n):
        row = np.zeros(nvars, dtype=float)
        row[pi_offset + i] = 1.0
        row[u_offset + i] = -1.0
        row[theta_idx] = 1.0
        rows.append(row)
        lows.append(0.0)
        ups.append(np.inf)

    row = np.zeros(nvars, dtype=float)
    row[:z_total] = np.concatenate(margins)
    row[theta_idx] = -float(instance.gamma)
    row[pi_offset : pi_offset + n] = -1.0
    rows.append(row)
    lows.append(0.0)
    ups.append(np.inf)

    constraints = opt.LinearConstraint(np.vstack(rows), np.array(lows), np.array(ups))
    t0 = time.perf_counter()
    res = opt.milp(
        c,
        integrality=integrality,
        bounds=opt.Bounds(lb, ub),
        constraints=constraints,
        options={"time_limit": float(time_limit), "mip_rel_gap": 1e-8},
    )
    runtime = time.perf_counter() - t0
    status = int(getattr(res, "status", -999))
    x = getattr(res, "x", None)
    objective = -float(res.fun) if getattr(res, "fun", None) is not None else float("nan")
    selections: Optional[List[int]] = None
    cert = float("nan")
    if x is not None:
        selections = []
        offset = 0
        for m_i in sizes:
            selections.append(int(np.argmax(x[offset : offset + m_i])))
            offset += m_i
        cert = compute_certificate(instance, selections)
    return {
        "status": highs_status_name(status),
        "certified": status == 0 and selections is not None and cert >= -1e-7,
        "objective": objective,
        "runtime_s": runtime,
        "mip_gap": float(getattr(res, "mip_gap", float("nan"))),
        "certificate_value": cert,
        "selections": selections,
        "n_variables": nvars,
        "n_constraints": len(rows),
        "message": str(getattr(res, "message", "")),
    }


def solve_fixed_theta_highs(
    instance: PricingInstance, theta: float, *, time_limit: float = 20.0
) -> Dict[str, object]:
    opt = scipy_opt()
    if opt is None or not hasattr(opt, "milp"):
        return {"status": "UNAVAILABLE", "certified": False}
    values, costs, _, capacity = fixed_theta_costs(instance, theta)
    if capacity < -EPS:
        return {"status": "INFEASIBLE_CAPACITY", "certified": True}
    sizes = [len(v) for v in values]
    total = int(sum(sizes))
    c = -np.concatenate(values)
    integrality = np.ones(total, dtype=int)
    bounds = opt.Bounds(lb=np.zeros(total), ub=np.ones(total))
    a_eq = np.zeros((len(sizes), total), dtype=float)
    offset = 0
    for i, m_i in enumerate(sizes):
        a_eq[i, offset : offset + m_i] = 1.0
        offset += m_i
    a_ub = np.concatenate(costs)[None, :]
    constraints = [
        opt.LinearConstraint(a_eq, np.ones(len(sizes)), np.ones(len(sizes))),
        opt.LinearConstraint(a_ub, -np.inf, np.array([capacity])),
    ]
    t0 = time.perf_counter()
    res = opt.milp(
        c,
        integrality=integrality,
        bounds=bounds,
        constraints=constraints,
        options={"time_limit": float(time_limit), "mip_rel_gap": 1e-8},
    )
    runtime = time.perf_counter() - t0
    status = int(getattr(res, "status", -999))
    x = getattr(res, "x", None)
    objective = -float(res.fun) if getattr(res, "fun", None) is not None else float("nan")
    selections: Optional[List[int]] = None
    cert = float("nan")
    cost_used = float("nan")
    if x is not None:
        selections = []
        offset = 0
        for m_i in sizes:
            local = x[offset : offset + m_i]
            selections.append(int(np.argmax(local)))
            offset += m_i
        cert = compute_certificate(instance, selections)
        cost_used = float(sum(costs[i][j] for i, j in enumerate(selections)))
    return {
        "status": highs_status_name(status),
        "certified": status == 0 and selections is not None and cert >= -1e-7 and cost_used <= capacity + 1e-7,
        "objective": objective,
        "runtime_s": runtime,
        "mip_gap": float(getattr(res, "mip_gap", float("nan"))),
        "certificate_value": cert,
        "selections": selections,
    }


def solve_theta_enum_highs(
    instance: PricingInstance, *, time_limit_per_theta: float = 5.0, max_thetas: int = 220
) -> Dict[str, object]:
    candidates = raw_theta_candidates(instance)
    if candidates.size > max_thetas:
        return {
            "status": "SKIPPED_TOO_MANY_THETA",
            "certified": False,
            "theta_count": int(candidates.size),
            "runtime_s": 0.0,
        }
    best_obj = -np.inf
    best_sel: Optional[List[int]] = None
    statuses: List[str] = []
    evaluated = 0
    t0 = time.perf_counter()
    for theta in candidates:
        _, _, _, capacity = fixed_theta_costs(instance, float(theta))
        if capacity < -EPS:
            continue
        evaluated += 1
        res = solve_fixed_theta_highs(instance, float(theta), time_limit=time_limit_per_theta)
        statuses.append(str(res.get("status")))
        if not bool(res.get("certified")):
            return {
                "status": "NOT_CERTIFIED",
                "certified": False,
                "theta_count": int(candidates.size),
                "theta_evaluated_milp": evaluated,
                "runtime_s": time.perf_counter() - t0,
                "failed_status": res.get("status"),
            }
        obj = float(res["objective"])
        if obj > best_obj + EPS:
            best_obj = obj
            best_sel = list(res["selections"])  # type: ignore[arg-type]
    cert = compute_certificate(instance, best_sel) if best_sel is not None else float("nan")
    return {
        "status": "OPTIMAL" if best_sel is not None else "NO_FEASIBLE_THETA",
        "certified": best_sel is not None and cert >= -1e-7,
        "objective": float(best_obj),
        "runtime_s": time.perf_counter() - t0,
        "theta_count": int(candidates.size),
        "theta_evaluated_milp": evaluated,
        "certificate_value": float(cert),
        "selections": best_sel,
    }


def gamma_regime(n: int, gamma: int) -> str:
    if gamma == 0:
        return "0"
    if abs(gamma - int(math.floor(math.sqrt(n)))) <= 1:
        return r"$\lfloor\sqrt{n}\rfloor$"
    return r"$\lfloor0.1n\rfloor$"


